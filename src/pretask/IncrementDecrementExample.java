package pretask;

public class IncrementDecrementExample {
    public static void main(String[] args) {
        // Объявление целочисленной переменной
        int a = 10;

        // Вывод начального значения
        System.out.println("a = " + a);

        // Префиксный инкремент
        System.out.println("префиксный инкремент a = " + (++a));

        // Постфиксный инкремент
        System.out.println("постфиксный инкремент а = " + (a++));

        // Значение после постфиксного инкремента
        System.out.println("a после постфиксного инкремента: " + a);

        // Префиксный декремент
        System.out.println("префиксный декремент а = " + (--a));

        // Постфиксный декремент
        System.out.println("постфиксный декремент а = " + (a--));

        // Значение после постфиксного декремента
        System.out.println("a после постфиксного декремента: " + a);
    }
}
